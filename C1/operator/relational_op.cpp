#include<iostream>
using namespace std;

int main() {

    int first_num= 10;
    int second_num= 13;

    cout << (first_num > second_num) << endl;
    cout << (first_num < second_num) << endl;
    cout << (first_num >= second_num) << endl;
    cout << (first_num <= second_num) << endl;
    cout << (first_num != second_num) << endl;
    cout << (first_num == second_num) << endl;
}